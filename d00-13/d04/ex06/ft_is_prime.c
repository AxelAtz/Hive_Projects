int	ft_is_prime(int nb)
{
	int i;

	if (nb < 2 || ((nb % 2) == 0 && nb != 2))
	{
		return (0);
	}
	else
	{
		i = 3;
		if ((nb % i) == 0)
		{
			return (0);
		}
	}
	return (ft_is_prime(nb+2));
}
